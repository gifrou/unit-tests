package cz.itnetwork;

public class Kalkulacka {
    public int secti (int a, int b) {
        return a + b;
    }

    public int odecti (int a, int b) {
        return a - b;
    }

    public int vydel (int a, int b) {
        return a / b;
    }

    public int vynasob (int a, int b) {
        return a * b;
    }
}


